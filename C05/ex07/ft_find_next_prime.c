int	ft_is_prime(int nb)
{
	int	i;

	i = 3;
	while (i < nb / i)
	{	
		if (nb % i == 0)
			return (0);
		i = i + 1;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	if (nb % 2 == 0)
		nb++;
	while (ft_is_prime(nb) != 1)
		nb = nb + 2;
	return (nb);
}
