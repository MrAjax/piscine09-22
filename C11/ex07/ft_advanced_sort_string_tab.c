void	ft_advanced_sort_string_tab(char **tab, int (*cmp)(char *, char *))
{
	int		i;
	char	*tmp;
	int		j;

	i = 0;
	j = 0;
	while (tab[j])
		j++;
	while (i < j - 1)
	{
		if (((cmp)(tab[i], tab[i + 1])) > 0)
		{
			tmp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = tmp;
			i = 0;
		}
		else
			i++;
	}
}
