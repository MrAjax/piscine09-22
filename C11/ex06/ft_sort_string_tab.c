int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}

void	ft_sort_string_tab(char **tab)
{
	int		i;
	int		(*ptr)(char*, char*);
	char	*tmp;
	int		j;

	ptr = &ft_strcmp;
	i = 0;
	j = 0;
	while (tab[j])
		j++;
	while (i < j - 1)
	{
		if (((*ptr)(tab[i], tab[i + 1])) > 0)
		{
			tmp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = tmp;
			i = 0;
		}
		else
			i++;
	}
}
